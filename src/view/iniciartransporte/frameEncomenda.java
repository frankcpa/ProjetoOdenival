    private void populaTabelaEncomendas() {
        if (this.encomendaRepository == null) {
            this.encomendaRepository = new EncomendaRepository();
        }
        DefaultTableModel model = (DefaultTableModel) jtableEncomendas.getModel();
        model.setRowCount(0);
        List<EncomendaModel> listaDeEncomendas = this.encomendaRepository.buscarTodos();
        for (int i = 0; i < listaDeEncomendas.size(); i++) {
            model.addRow(new Object[]{
                listaDeEncomendas.get(i).getIdEncomenda(),
                listaDeEncomendas.get(i).getRemetente().getNomeFantasia(),
                listaDeEncomendas.get(i).getDestinatario().getNomeFantasia()
            });
        }
        jtableEncomendas.setModel(model);
    }
    

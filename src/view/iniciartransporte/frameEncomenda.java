    private void btnRelacionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRelacionarActionPerformed
        if (!txtDescricao.getText().equals("") && !txtPeso.getText().equals("")) {
            EncomendaModel encomendaModel = new EncomendaModel();
            ComboItem item;
            if (txtIdEncomenda.getText().equals("")){
                if (this.encomendaRepository == null) {
                    this.encomendaRepository = new EncomendaRepository();
                }
                if (this.empresaRepository == null) {
                    this.empresaRepository = new EmpresaRepository();
                }

                item = (ComboItem) comboBoxRemetente.getSelectedItem();
                long remetenteID = Long.parseLong(item.getValue());
                EmpresaModel remetente = empresaRepository.buscarPorId(remetenteID);

                item = (ComboItem) comboBoxDestinatario.getSelectedItem();
                long destinatarioID = Long.parseLong(item.getValue());
                EmpresaModel destinatario = empresaRepository.buscarPorId(destinatarioID);

                encomendaModel.setDestinatario(destinatario);
                encomendaModel.setRemetente(remetente);
                encomendaModel.setFinalizada(false);

                encomendaRepository.salvar(encomendaModel);
                
                txtIdEncomenda.setText(encomendaModel.getIdEncomenda()+"");
            }else{
                encomendaModel = encomendaRepository.buscarPorId(Long.parseLong(txtIdEncomenda.getText()));
            }
            
            if (this.encomendaxVolumeRepository == null) {
                this.encomendaxVolumeRepository = new EncomendaxVolumeRepository();
            }
            if (this.volumeRepository == null) {
                this.volumeRepository = new VolumeRepository();
            }
            
            EncomendaxVolumeModel encomendaxVolumeModel = new EncomendaxVolumeModel();
            encomendaxVolumeModel.setDescricaoProduto(txtDescricao.getText());
            encomendaxVolumeModel.setPesototal(Float.parseFloat(txtPeso.getText()));
            encomendaxVolumeModel.setFinalizada(false);
            
            item = (ComboItem) comboBoxVolume.getSelectedItem();
            long volumeID = Long.parseLong(item.getValue());
            VolumeModel volume = volumeRepository.buscarPorId(volumeID);
            encomendaxVolumeModel.setVolume(volume);
            
            encomendaxVolumeModel.setEncomenda(encomendaModel);
           
            encomendaxVolumeRepository.salvar(encomendaxVolumeModel);
           
            JOptionPane.showMessageDialog(null, "Salvo com sucesso!");
           
            //txtIdEncomenda.setText("");
            populaTabelaVolumesRelacionados(encomendaModel.getIdEncomenda());
            
            encomendaModel = null;
            encomendaxVolumeModel = null;
            botaoCancelarEdicao.setVisible(false);
            botaoCadastrarCargo.setLabel("Salvar");
            comboBoxDestinatario.setEnabled(false);
            comboBoxRemetente.setEnabled(false);
            comboBoxVolume.setSelectedIndex(0);
            txtDescricao.setText("");
            txtPeso.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "Informe o Peso e a Descrição");
        }
    }//GEN-LAST:event_btnRelacionarActionPerformed



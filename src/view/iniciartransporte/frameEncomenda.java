    private void populaComboBoxRemetente(EmpresaModel empresaSelecionada) {
        if (this.empresaRepository == null) {
            this.empresaRepository = new EmpresaRepository();
        }
        List<EmpresaModel> listaDeEmpresas = this.empresaRepository.buscarTodos();
        Object objs[] = new Object[listaDeEmpresas.size()];
        frameEncomenda.ComboItem item;
        int itemSelecionado = 0;
        //objs[0] = new ComboItem("0", "Selecione");
        for (int i = 0; i < listaDeEmpresas.size(); i++) {
            item = new ComboItem("" + listaDeEmpresas.get(i).getIdEmpresa(), listaDeEmpresas.get(i).getNomeFantasia());
            objs[i] = item;
            if (empresaSelecionada != null) {
                if (empresaSelecionada.getNomeFantasia().toLowerCase().trim().equals(listaDeEmpresas.get(i).getNomeFantasia().toLowerCase().trim())) {
                    itemSelecionado = i;
                }
            }
        }
        DefaultComboBoxModel model2 = new DefaultComboBoxModel(objs);
        comboBoxRemetente.setModel(model2);
        if (empresaSelecionada != null) {
            comboBoxRemetente.setSelectedIndex(itemSelecionado);
        }
    }
    

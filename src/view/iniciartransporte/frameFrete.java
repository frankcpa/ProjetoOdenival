    private void populaComboBoxMotorista(FuncionarioModel funcionarioSelecionado) {
        if (this.funcionarioRepository == null) {
            this.funcionarioRepository = new FuncionarioRepository();
        }
        List<FuncionarioModel> listaDeFuncionarios = this.funcionarioRepository.buscarTodosMotorista();
        Object objs[] = new Object[listaDeFuncionarios.size()];
        frameFrete.ComboItem item;
        int itemSelecionado = 0;
        
        for (int i = 0; i < listaDeFuncionarios.size(); i++) {
            item = new ComboItem("" + listaDeFuncionarios.get(i).getIdPessoa(), listaDeFuncionarios.get(i).getNome());
            objs[i] = item;
            if (funcionarioSelecionado != null) {
                if (funcionarioSelecionado.getNome().toLowerCase().trim().equals(listaDeFuncionarios.get(i).getNome().toLowerCase().trim())) {
                    itemSelecionado = i;
                }
            }
        }
        DefaultComboBoxModel model2 = new DefaultComboBoxModel(objs);
        comboBoxMotorista.setModel(model2);
        if (funcionarioSelecionado != null) {
            comboBoxMotorista.setSelectedIndex(itemSelecionado);
        }
    }
    

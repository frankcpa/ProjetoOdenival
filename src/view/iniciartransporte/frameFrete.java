    private void btnRelacionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRelacionarActionPerformed
        if (!txtObservacao.getText().equals("") && !txtData.getText().equals("")) {
            if (this.freteRepository == null) {
                this.freteRepository = new FreteRepository();
            }
            FreteModel freteModel = new FreteModel();
            ComboItem item;
            if (txtidFrete.getText().equals("")){
                if (this.veiculoRepository== null) {
                    this.veiculoRepository = new VeiculoRepository();
                }
                if (this.funcionarioRepository == null) {
                    this.funcionarioRepository = new FuncionarioRepository();
                }
                
                item = (ComboItem) comboBoxMotorista.getSelectedItem();
                long motoristaId = Long.parseLong(item.getValue());
                FuncionarioModel funcionario = funcionarioRepository.buscarPorId(motoristaId);

                item = (ComboItem) comboBoxVeiculo.getSelectedItem();
                long veiculoId = Long.parseLong(item.getValue());
                VeiculoModel veiculoModel = veiculoRepository.buscarPorId(veiculoId);

                freteModel.setMotorista(funcionario);
                freteModel.setVeiculo(veiculoModel);
                freteModel.setFinalizada(false);
                freteModel.setObservacao(txtObservacao.getText());
                
                SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
                Date dataFormatada = null;
                try { 
                    dataFormatada = formato.parse(txtData.getText());
                } catch (ParseException ex) {
                    Logger.getLogger(frameFrete.class.getName()).log(Level.SEVERE, null, ex);
                }
                freteModel.setPrevisaoSaida(dataFormatada);
                
                this.freteRepository.salvar(freteModel);

                txtidFrete.setText(freteModel.getIdFrete()+"");
            }else{
                freteModel = freteRepository.buscarPorId(Long.parseLong(txtidFrete.getText()));
            }
            
            if (this.fretexEncomendaRepository == null) {
                this.fretexEncomendaRepository = new FretexEncomendaRepository();
            }
            if (this.encomendaRepository == null) {
                this.encomendaRepository = new EncomendaRepository();
            }

            FretexEncomendaModel fretexEncomendaModel = new FretexEncomendaModel();
            
            if(txtValorFrete.getText().equals("")){
                fretexEncomendaModel.setValorFrete(0);
            }else {
                fretexEncomendaModel.setValorFrete(Float.parseFloat(txtValorFrete.getText()));
            }
            
            if(txtValorReembolso.getText().equals("")){
                fretexEncomendaModel.setValorReembolso(0);
            }else {
                fretexEncomendaModel.setValorReembolso(Float.parseFloat(txtValorReembolso.getText()));
            }
            
            fretexEncomendaModel.setFinalizada(false);

            item = (ComboItem) comboBoxEncomenda.getSelectedItem();
            long encomendaID = Long.parseLong(item.getValue());
            EncomendaModel encomenda = encomendaRepository.buscarPorId(encomendaID);
            fretexEncomendaModel.setEncomenda(encomenda);

            fretexEncomendaModel.setFrete(freteModel);

            fretexEncomendaRepository.salvar(fretexEncomendaModel);

            JOptionPane.showMessageDialog(null, "Salvo com sucesso!");


            populaTabelaEncomendasRelacionadas(freteModel.getIdFrete());

            freteModel = null;
            fretexEncomendaModel = null;
            botaoCancelarEdicao.setVisible(false);
            btnFinalizarEncomenda.setVisible(true);
            comboBoxVeiculo.setEnabled(false);
            comboBoxMotorista.setEnabled(false);
            txtData.setEnabled(false);
            txtObservacao.setEnabled(false);
            comboBoxEncomenda.setSelectedIndex(0);
            txtValorFrete.setText("");
            txtValorReembolso.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "Informe a data e a observação");
        }
    }//GEN-LAST:event_btnRelacionarActionPerformed

